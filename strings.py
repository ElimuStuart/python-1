my_name = 'John Doe'
print(my_name)

print(my_name[2]) # slicing
print(len(my_name)) # length of string
print(my_name.upper()) # make uppercase
print(my_name.find('o')) # returns the index of the first occurance of character
print(my_name.split(' ')) # splits by a space(can also split by comma, colon etc..), returns a list

